		Scroll2.prototype={
			init:function(){
				this.ul.css("width",this.li.outerWidth()*this.li.length);
				this.event();
			},
			event:function(){
				var me=this;
				this.l.click(function(){
					if(!me.done || me.li.length<5) return;
					me.done=!me.done;
					me.ul.animate({left:-me.li.outerWidth()},"fast",function(){
						
						me.ul=me.id.find("ul");
						me.li=me.id.find("li");
						me.ul.append(me.li.eq(0));
						me.ul.css("left",0);
						me.done=!me.done;
